let palabrasPartida = [];
let indice = 0;
let puntaje = 0;
let ayudas = 0;
let valor = 1;
let correctas = 0;
let incorrectas = 0;

function mostrarDificultades(){

    document
        .getElementById("intro")
        .classList.add("hidden");

    document
        .getElementById("dificultad")
        .classList.remove("hidden");

}

function obtenerPreguntas(dificultad){

    return [...banco[dificultad]]
        .sort(() => Math.random() - 0.5)
        .slice(0,10);

}

function iniciarJuego(dificultad){

    document
        .getElementById("dificultad")
        .classList.add("hidden");

    document
        .getElementById("juego")
        .classList.remove("hidden");

    palabrasPartida =
        obtenerPreguntas(dificultad);

    indice = 0;

    mostrar();

}

function mezclar(texto){

    let resultado;

    do{

        let letras = texto.split("");

        for(let i=letras.length-1;i>0;i--){

            let j =
            Math.floor(
                Math.random()*(i+1)
            );

            [letras[i],letras[j]] =
            [letras[j],letras[i]];
        }

        resultado = letras.join("");

    }
    while(resultado === texto);

    return resultado.split("").join(" ");

}

function mostrar(){

    let actual =
        palabrasPartida[indice];

    progreso.textContent =
        `Concepto ${indice+1} de ${palabrasPartida.length}`;

    let porcentaje =
        ((indice+1) /
        palabrasPartida.length) * 100;

    document
        .getElementById("progresoBarra")
        .style.width =
        porcentaje + "%";

    letras.textContent =
        mezclar(actual.palabra);

    respuesta.value = "";

    ayuda.textContent = "";

    valor = 1;

    document
        .getElementById("valorPuntos")
        .textContent =
        "Valor actual: 1 punto";

}

function usarAyuda(){

    let actual =
        palabrasPartida[indice];

    if(valor===1){

        ayuda.textContent =
            actual.definicion;

        valor = 0.75;

    }

    else if(valor===0.75){

        ayuda.textContent +=
        " | Empieza por: " +
        actual.palabra[0];

        valor = 0.5;

    }

    document
        .getElementById("valorPuntos")
        .textContent =
        "Valor actual: " +
        valor +
        " puntos";

    ayudas++;

}

function verificarRespuesta(){

    let actual =
        palabrasPartida[indice];

    let respuestaUsuario =
        respuesta.value
        .trim()
        .toUpperCase();

    if(respuestaUsuario === actual.palabra){

        puntaje += valor;

        correctas++;

        document.getElementById("feedbackTitulo").textContent =
    "✅ Correcto";

    }
    else{

        incorrectas++;

        document.getElementById("feedbackTitulo").textContent =
    "❌ Incorrecto";

    }

   document.getElementById("feedbackPalabra").textContent =
    actual.palabra;

    document.getElementById("feedbackCategoria").textContent =
    "Categoría: " + actual.categoria;

    document.getElementById("feedbackDefinicion").textContent =
    actual.definicion;

    document
    .getElementById("juego")
    .classList.add("hidden");

    document
    .getElementById("feedback")
    .classList.add("hidden");
}

function siguientePregunta(){

    document
    .getElementById("feedback")
    .classList.add("hidden");

    indice++;

    if(indice >= palabrasPartida.length){

        fin();

    }
    else{

     document
    .getElementById("juego")
    .classList.remove("hidden");

        mostrar();

    }

}


function fin(){

    document
        .getElementById("juego")
        .classList.add("hidden");

    document
        .getElementById("resultado")
        .classList.remove("hidden");

    puntajeFinal.textContent =
        "Puntaje: " +
        puntaje.toFixed(2);

    estadisticas.innerHTML =

    "Correctas: " +
    correctas +

    "<br>Incorrectas: " +
    incorrectas +

    "<br>Ayudas utilizadas: " +
    ayudas;

}